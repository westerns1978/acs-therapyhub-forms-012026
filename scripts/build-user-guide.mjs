#!/usr/bin/env node
// Assembles docs/user-guide/ACS-TherapyHub-User-Guide.md from the numbered
// section files (00-*.md … NN-*.md), in numeric order.
//
// DERIVED ARTIFACT: the combined file is generated, never hand-edited. The
// numbered section files are the single source of truth — the app renders them
// directly (pages/help/guide.ts imports each via Vite `?raw`). Edit a section
// file, then regenerate the combined guide with:  npm run build:guide
//
// Node only, no dependencies.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const guideDir = join(here, '..', 'docs', 'user-guide');
const OUTPUT = 'ACS-TherapyHub-User-Guide.md';

// Canonical sections = files named `NN-*.md` (two leading digits). The combined
// output (ACS-TherapyHub-User-Guide.md) and any "New folder" are excluded by the
// pattern. Lexical sort on a zero-padded numeric prefix == numeric order.
const sections = readdirSync(guideDir)
  .filter((f) => /^\d{2}-.*\.md$/.test(f))
  .sort();

if (sections.length === 0) {
  console.error(`[build:guide] no NN-*.md section files found in ${guideDir}`);
  process.exit(1);
}

const header = [
  '<!--',
  '  AUTO-GENERATED — do not edit this file by hand.',
  '',
  '  This combined guide is assembled from the numbered section files in',
  `  docs/user-guide/ (00-*.md … ${sections[sections.length - 1]}). Those numbered`,
  '  files are the single source of truth — the app renders them directly',
  '  (pages/help/guide.ts imports each via Vite ?raw). Edit the section files,',
  '  then regenerate this file with:  npm run build:guide',
  '-->',
  '',
  '',
].join('\n');

// Each section file starts with its own `#` H1; a thematic break makes the seam
// between sections explicit in the assembled document.
const body = sections
  .map((f) => readFileSync(join(guideDir, f), 'utf8').trimEnd())
  .join('\n\n---\n\n');

writeFileSync(join(guideDir, OUTPUT), header + body + '\n', 'utf8');
console.log(
  `[build:guide] wrote ${OUTPUT} from ${sections.length} section(s): ${sections.join(', ')}`,
);
